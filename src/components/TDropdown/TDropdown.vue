<template>
  <div :id="id" ref="dropdown" :class="getClasses">
    <div class="dropdown-trigger">
      <button
        class="button"
        aria-haspopup="true"
        aria-controls="dropdown-menu"
        @click="toggleActive"
      >
        <span>{{ text }}</span>
        <span class="icon is-small">
          <t-icon :preserve-defaults="!overrideDefaults" :icon="icon"></t-icon>
        </span>
      </button>
    </div>
    <div class="dropdown-menu" id="dropdown-menu" role="menu">
      <div class="dropdown-content">
        <slot></slot>
      </div>
    </div>
  </div>
</template>

<script>
import alignment from "../../mixins/alignment";
import common from "../../mixins/common";
import icons from "../../mixins/icons";
import CssArchitect from "../../utils/css-architect";
import TIcon from "../TIcon/TIcon";

export default {
  name: "t-dropdown",
  components: { TIcon },
  mixins: [common, alignment, icons],
  props: {
    text: {
      type: String
    },
    icon: {
      type: String,
      default: function() {
        return this.$thisvui.icons.arrowDown;
      }
    },
    isUp: {
      type: Boolean,
      default: false
    },
    isHoverable: {
      type: Boolean,
      default: false
    },
    isActive: {
      type: Boolean,
      default: false
    }
  },
  data() {
    return {
      isDropdownActive: false
    };
  },
  computed: {
    /**
     * Dynamically build the css classes for the target element
     * @returns { A String with the chained css classes }
     */
    getClasses: function() {
      const cssArchitect = new CssArchitect("dropdown");
      cssArchitect.addClass(this.getAlignmentModifiers);
      cssArchitect.addClass("is-up", this.isUp);
      cssArchitect.addClass("is-hoverable", this.isHoverable);
      cssArchitect.addClass(
        "is-active",
        this.isActive || this.isDropdownActive
      );
      return cssArchitect.getClasses();
    }
  },
  methods: {
    toggleActive() {
      if (!this.isHoverable && !this.isActive) {
        this.isDropdownActive = !this.isDropdownActive;
      }
    }
  }
};
</script>
